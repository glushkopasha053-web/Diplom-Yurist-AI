def parse_website(url):

    print(f"\n🌐 Парсимо сайт: {url}")

    try:

        # ============================================
        # HEADERS
        # ============================================

        headers = {

            "User-Agent": (

                "Mozilla/5.0 "
                "(Windows NT 10.0; Win64; x64) "
                "AppleWebKit/537.36 "
                "(KHTML, like Gecko) "
                "Chrome/120.0 Safari/537.36"

            )

        }

        # ============================================
        # ЗАПИТ ДО САЙТУ
        # ============================================

        response = requests.get(

            url,
            headers=headers,
            timeout=20

        )

        # якщо сайт повернув помилку
        response.raise_for_status()

        # ============================================
        # HTML -> BEAUTIFULSOUP
        # ============================================

        soup = BeautifulSoup(

            response.text,
            "html.parser"

        )

        # ============================================
        # ВИДАЛЯЄМО СМІТТЯ
        # ============================================

        for tag in soup([

            "script",
            "style",
            "header",
            "footer",
            "nav",
            "aside",
            "img",
            "svg",
            "button",
            "form"

        ]):

            tag.decompose()

        # ============================================
        # ОСНОВНИЙ ТЕКСТ ЗАКОНУ
        # ============================================

        main_content = soup.find(

            "div",
            class_="doc-body"

        )

        # якщо не знайшли
        if not main_content:

            print("❌ Основний текст не знайдено")

            return

        # ============================================
        # ШУКАЄМО БЛОКИ
        # ============================================

        paragraphs = main_content.find_all([

            "p",
            "div"

        ])

        print(f"📄 Знайдено блоків: {len(paragraphs)}")

        # ============================================
        # ЛІЧИЛЬНИК
        # ============================================

        saved_count = 0

        # ============================================
        # ПРОХОДИМО ПО БЛОКАХ
        # ============================================

        for paragraph in paragraphs:

            try:

                # ------------------------------------
                # ТЕКСТ
                # ------------------------------------

                text = paragraph.get_text(

                    " ",
                    strip=True

                )

                # ------------------------------------
                # ОЧИЩЕННЯ
                # ------------------------------------

                text = " ".join(text.split())

                # ------------------------------------
                # ПРОПУСКАЄМО СМІТТЯ
                # ------------------------------------

                if len(text) < 100:

                    continue

                # ------------------------------------
                # ПРОПУСКАЄМО МЕНЮ
                # ------------------------------------

                bad_words = [

                    "facebook",
                    "telegram",
                    "twitter",
                    "youtube",
                    "instagram",
                    "підписатися",
                    "кабінет",
                    "увійти"

                ]

                skip = False

                for word in bad_words:

                    if word.lower() in text.lower():

                        skip = True

                        break

                if skip:

                    continue

                # ------------------------------------
                # TITLE
                # ------------------------------------

                title = text[:100]

                # ------------------------------------
                # KEYWORDS
                # ------------------------------------

                keywords = """

                суд,
                право,
                кодекс,
                закон,
                адвокат,
                поліція,
                кримінальний процес,
                цивільний процес,
                Україна

                """

                # ------------------------------------
                # ЗБЕРЕЖЕННЯ В БАЗУ
                # ------------------------------------

                add_article(

                    title=title,
                    keywords=keywords,
                    content=text

                )

                saved_count += 1

                print(f"✅ Додано: {title[:70]}")

            except Exception as error:

                print("❌ Помилка блоку:", error)

        # ============================================
        # ГОТОВО
        # ============================================

        print(f"\n✅ УСПІШНО ЗБЕРЕЖЕНО: {saved_count}")

    except Exception as error:

        print("\n❌ ПОМИЛКА ПАРСИНГУ")

        print(error)


